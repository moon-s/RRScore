def main() -> None:
    args = parse_args()
    args.outdir.mkdir(parents=True, exist_ok=True)
    table_dir = args.outdir / "tables"
    table_dir.mkdir(exist_ok=True)

    files = find_h5ad_files(args.split_dir, args.manifest, args.pattern)
    if not files:
        raise SystemExit(f"No h5ad files found in {args.split_dir}")
    print(f"Found split h5ad files: {len(files):,}")

    risk, protective, gs_sub = load_gene_sets(args.gene_set_file, args.gene_set)
    ppi_genes = load_gene_list(args.ppi_gene_file, args.ppi_gene_column)
    if ppi_genes is None:
        print("[WARN] --ppi-gene-file omitted. Using risk/protective gene union only as feature universe.")
        selected_genes = set(risk | protective)
    else:
        selected_genes = set(ppi_genes | risk | protective)  # ensure program genes are retained if present in h5ad
    print(f"risk genes={len(risk):,}; protective genes={len(protective):,}; selected/PPI universe={len(selected_genes):,}")

    if args.dry_run:
        print("Dry-run files:")
        for p in files[:20]: print(p)
        return

    qc, common_genes, _ = first_pass_qc(files, selected_genes, args)
    qc_path = table_dir / "mssm_qc_passed_cells_before_balancing.tsv.gz"
    qc.to_csv(qc_path, sep="\t", index=False, compression="gzip")
    print(f"QC-passed metadata written: {qc_path}; cells={len(qc):,}")

    sampled = balanced_sample(qc, args)
    sampled_path = table_dir / "mssm_balanced_qc_passed_cells.tsv.gz"
    sampled.to_csv(sampled_path, sep="\t", index=False, compression="gzip")
    print(f"Balanced sampled cells written: {sampled_path}; cells={len(sampled):,}")

    # Use genes common to all processed split files so the output matrix has stable rows.
    genes = sorted(set(common_genes))
    # Keep all gene-set genes that are present in common_genes; report missing.
    present_risk = sorted(risk & set(genes))
    present_prot = sorted(protective & set(genes))
    gene_qc = pd.DataFrame({
        "metric": ["selected_genes_common_to_files", "risk_present", "protective_present", "risk_missing", "protective_missing"],
        "value": [len(genes), len(present_risk), len(present_prot), len(risk - set(genes)), len(protective - set(genes))],
    })
    gene_qc.to_csv(table_dir / "mssm_balanced_gene_qc.tsv", sep="\t", index=False)
    pd.DataFrame({"gene": genes}).to_csv(table_dir / "mssm_balanced_expression_genes.tsv", sep="\t", index=False)


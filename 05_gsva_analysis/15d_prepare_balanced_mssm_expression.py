"""
Step 15d. Build a QC-passed, balanced MSSM single-cell expression matrix for
one global GSVA run.

Purpose
-------
This script is intended to replace split-wise per-cell GSVA for MSSM. It:
  1. reads MSSM split .h5ad files generated by Step 15a,
  2. restricts expression features to PPI-network genes,
  3. applies per-cell sparsity QC using minimum detected feature fraction,
  4. samples up to N QC-passed cells per cell type, balanced across diseases,
  5. writes a balanced expression table suitable for one global R/GSVA call.

Default QC requested in discussion:
  detected PPI-network feature fraction > 20%
  up to 10,000 QC-passed cells per cell type

Important
---------
The output expression table is genes x cells, because GSVA expects genes as
rows and samples/cells as columns.

For very large PPI gene universes and many sampled cells, the TSV can be large.
The script writes a cell x gene float32 memmap internally first, then streams a
genes x cells TSV.gz.
"""


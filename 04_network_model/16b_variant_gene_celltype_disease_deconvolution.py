"""Step 16: variant -> gene -> cell type / disease contribution deconvolution.

This script extends the previous variant contribution decomposition workflow to produce
context-specific tables for single-cell/pseudobulk RLS program activity.

Inputs are intentionally explicit. If an expected file or required column is missing,
the script raises a clear error instead of silently guessing.

Core workflow
-------------
1. Load an existing variant-gene contribution table, preferably generated by
   08_variant_contribution_decomposition.py:
       gwas_linked_regulatory_variant_contributions.tsv.gz
2. Load a cell-type/disease cscore table from Step14/Step15.
3. Aggregate cscore by cell_type and disease.
4. Compute context-specific contribution:
       context_signed_contribution = signed_variant_contribution * context_cscore
   and related normalized/ranked metrics.
5. Write a full variant -> gene -> cell_type/disease table.
6. Select representative variants and build a compact network-ready table/plot.

Notes
-----
- The input variant contribution table already contains the decomposition of predicted
  node beta into GWAS-linked regulatory variant contributions. This script does not
  recompute Borzoi deltas or LD weights unless you rerun Step08 upstream.
- Context specificity comes from observed cscore activity in a disease/cell type.
- If the cscore table contains one row per cell, the script aggregates robustly.
- If disease labels contain composite diagnoses separated by ||, ;, comma, or /,
  the script can expand them so one sample/cell contributes to multiple disease groups.
"""
